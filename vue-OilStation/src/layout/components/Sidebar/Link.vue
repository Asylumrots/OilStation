
<template>
  <!-- eslint-disable vue/require-component-is -->
  <!--v-bind-->
  <component v-bind="linkProps(to)" v-bind:is="s">
    <slot />
  </component>
</template>

<script>
import { isExternal } from '@/utils/validate'

export default {
  props: {
    to: {
      type: String,
      required: true
    }
  },
  methods: {
    s(){},//v-bind:is为component必须值
    linkProps(url) {
      if (isExternal(url)) {
        return {
          is: 'a',
          href: url,
          target: '_blank',
          rel: 'noopener'
        }
      }
      return {
        is: 'router-link',
        to: url
      }
    }
  }
}
</script>
